function operacoesMatematicas(x, y) {
    let soma = x + y
    let sub = x - y
    let mult = x * y
    let div = x / y
    console.log(`O resultado da soma de ${x} + ${y} = ${soma}`)
    console.log(`O resultado da soma de ${x} - ${y} = ${sub}`)
    console.log(`O resultado da soma de ${x} x ${y} = ${mult}`)
    console.log(`O resultado da soma de ${x} : ${y} = ${div}`)
}

operacoesMatematicas(4, 4)